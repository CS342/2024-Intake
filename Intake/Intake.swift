import Spezi
import SwiftUI


@main
struct Intake: App {
    @UIApplicationDelegateAdaptor(IntakeDelegate.self) var appDelegate
    @AppStorage(StorageKeys.onboardingFlowComplete) var completedOnboardingFlow = false

    @State var navigationPath = NavigationPathWrapper()
    @State var data = DataStore()
    @State var reachedEnd = ReachedEndWrapper()
    @State var loaded = LoadedWrapper()

    
    var body: some Scene {
        WindowGroup {
            ZStack {
                if completedOnboardingFlow {
                    HomeView()
                } else {
                    EmptyView()
                }
            }
                .sheet(isPresented: !$completedOnboardingFlow) {
                    OnboardingFlow()
                }
                .testingSetup()
                .spezi(appDelegate)
                .environment(navigationPath)
                .environment(data)
                .environment(reachedEnd)
                .environment(loaded)
        }
    }
}
